import unittest
import pykicadlib.symbol.element


class TestSymbolElementFromStrField(unittest.TestCase):
    def test_parser(self):
        test = pykicadlib.symbol.element.from_str('F1 "Text" 10 20 50 H V C CNN "Name"')
        self.assertIsInstance(test, pykicadlib.symbol.element.field)
        self.assertEqual(test.type, pykicadlib.symbol.type.field.name)
        self.assertEqual(test.value, 'Text')
        self.assertEqual(test.x, 10)
        self.assertEqual(test.y, 20)
        self.assertEqual(test.size, 50)
        self.assertEqual(test.orientation, pykicadlib.symbol.type.orientation.horizontal)
        self.assertEqual(test.visibility, pykicadlib.symbol.type.visibility.visible)
        self.assertEqual(test.hjustify, pykicadlib.symbol.type.hjustify.center)
        self.assertEqual(test.vjustify, pykicadlib.symbol.type.vjustify.center)
        self.assertEqual(test.style, pykicadlib.symbol.type.style.none)


class TestSymbolElementFromStrPolygon(unittest.TestCase):
    def test_parser(self):
        test = pykicadlib.symbol.element.from_str('P 3 0 1 0 -50 50 50 0 -50 -50 F')
        self.assertIsInstance(test, pykicadlib.symbol.element.polygon)
        self.assertEqual(len(test.points), 3)
        self.assertEqual(str(test.points[0]), '-50 50')
        self.assertEqual(str(test.points[1]), '50 0')
        self.assertEqual(str(test.points[2]), '-50 -50')
        self.assertEqual(test.unit, 0)
        self.assertEqual(test.representation, pykicadlib.symbol.type.representation.normal)
        self.assertEqual(test.fill, pykicadlib.symbol.type.fill.foreground)

        test = pykicadlib.symbol.element.from_str('P 2 0 1 0 50 50 50 -50 N')
        self.assertIsInstance(test, pykicadlib.symbol.element.polygon)
        self.assertEqual(len(test.points), 2)
        self.assertEqual(str(test.points[0]), '50 50')
        self.assertEqual(str(test.points[1]), '50 -50')
        self.assertEqual(test.unit, 0)
        self.assertEqual(test.representation, pykicadlib.symbol.type.representation.normal)
        self.assertEqual(test.fill, pykicadlib.symbol.type.fill.none)


