class TestSymbolElementFromStrPin(unittest.TestCase):
    def test_pin(self):
        test = pykicadlib.symbol.element.from_str('X TO 1 -200 0 150 R 40 40 1 1 P')
        self.assertIsInstance(test, pykicadlib.symbol.element.pin)
        self.assertEqual(test.x, -200)
        self.assertEqual(test.y, 0)
        self.assertEqual(test.name, 'TO')
        self.assertEqual(test.number, '1')
        self.assertEqual(test.length, 150)
        self.assertEqual(test.direction, pykicadlib.symbol.type.direction.left)
        self.assertEqual(test.nameSize, 40)
        self.assertEqual(test.numberSize, 40)
        self.assertEqual(test.unit, 1)
        self.assertEqual(test.representation, pykicadlib.symbol.type.representation.normal)
        self.assertEqual(test.electric, pykicadlib.symbol.type.electric.passive)
        self.assertEqual(test.shape, pykicadlib.symbol.type.shape.line)
        self.assertTrue(test.visible)

        test = pykicadlib.symbol.element.from_str('X K 2 200 0 150 L 40 40 1 1 P')
        self.assertIsInstance(test, pykicadlib.symbol.element.pin)
        self.assertEqual(test.x, 200)
        self.assertEqual(test.y, 0)
        self.assertEqual(test.name, 'K')
        self.assertEqual(test.number, '2')
        self.assertEqual(test.length, 150)
        self.assertEqual(test.direction, pykicadlib.symbol.type.direction.right)
        self.assertEqual(test.nameSize, 40)
        self.assertEqual(test.numberSize, 40)
        self.assertEqual(test.unit, 1)
        self.assertEqual(test.representation, pykicadlib.symbol.type.representation.normal)
        self.assertEqual(test.electric, pykicadlib.symbol.type.electric.passive)
        self.assertEqual(test.shape, pykicadlib.symbol.type.shape.line)
        self.assertTrue(test.visible)

        test = pykicadlib.symbol.element.from_str('X 0 1 0 0 0 R 40 40 1 1 W NC')
        self.assertIsInstance(test, pykicadlib.symbol.element.pin)
        self.assertEqual(test.x, 0)
        self.assertEqual(test.y, 0)
        self.assertEqual(test.name, '0')
        self.assertEqual(test.number, '1')
        self.assertEqual(test.length, 0)
        self.assertEqual(test.direction, pykicadlib.symbol.type.direction.left)
        self.assertEqual(test.nameSize, 40)
        self.assertEqual(test.numberSize, 40)
        self.assertEqual(test.unit, 1)
        self.assertEqual(test.representation, pykicadlib.symbol.type.representation.normal)
        self.assertEqual(test.electric, pykicadlib.symbol.type.electric.power_input)
        self.assertEqual(test.shape, pykicadlib.symbol.type.shape.clock)
        self.assertFalse(test.visible)

        test = pykicadlib.symbol.element.from_str('X ~ 2 0 -250 200 U 40 40 1 1 P')
        self.assertIsInstance(test, pykicadlib.symbol.element.pin)
        self.assertEqual(test.x, 0)
        self.assertEqual(test.y, -250)
        self.assertEqual(test.name, '~')
        self.assertEqual(test.number, '2')
        self.assertEqual(test.length, 200)
        self.assertEqual(test.direction, pykicadlib.symbol.type.direction.down)
        self.assertEqual(test.nameSize, 40)
        self.assertEqual(test.numberSize, 40)
        self.assertEqual(test.unit, 1)
        self.assertEqual(test.representation, pykicadlib.symbol.type.representation.normal)
        self.assertEqual(test.electric, pykicadlib.symbol.type.electric.passive)
        self.assertEqual(test.shape, pykicadlib.symbol.type.shape.line)
        self.assertTrue(test.visible)

